package chapter02;

public class code02_07 {
    public static void main(String[] args) {
        int num1 = 100;
        int num2 = 50;

        int result = num1 + num2;
        System.out.printf("%d + %d = %d\n",num1, num2, result);

        result = num1 - num2;
        System.out.printf("%d - %d = %d\n",num1, num2, result);

        result = num1 * num2;
        System.out.printf("%d * %d = %d\n",num1, num2, result);

        result = num1 / num2;
        System.out.printf("%d / %d = %d\n",num1, num2, result);











    }
}
